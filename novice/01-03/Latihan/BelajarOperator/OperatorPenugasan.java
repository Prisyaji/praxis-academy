public class OperatorPenugasan {
    public static void main(String[] args){
        int a, b;
        
        //Pengisian Nilai
        a = 5;
        b = 10;

        // Penambahan 
        b += a;
        //Sekarang b = 15
        System.out.println("Penambahan : " + b);

        //Pengurangan 
        b -= a;
        // sekarang b = 5
        System.out.println("Pengurangan : " + b);

        //Perkalian
        b *= a;
        //Sekarang b = 50
        System.out.println("Perkalian : " + b);
    }
}