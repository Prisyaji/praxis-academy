package Serius;

public class Balok { 
    public static void main(String[] args) {
        // Deklarasi Variabel
        int panjang, lebar, tinggi, volumeBalok;

        // Deklarasi Nilai Variabel
        panjang = 5;
        lebar = 4;
        tinggi = 7;

        // Proses
        volumeBalok = panjang * lebar * tinggi;

        // output
        System.out.println("Volume Balok: " + volumeBalok);
    }
}